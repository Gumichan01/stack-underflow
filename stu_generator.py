def generate_dict():
    """
        Generate the dictionary that contains different pairs (T, K)
        T is the name of the tag
        K is the set of keywords related to the tag, generated by stu_dictionary.py
    """
    pairs = []
    with open(stu_misc.DICT_FILE, 'r') as f:
        for line in f.readlines():
            row  = line.strip(stu_misc.ENDL).split(stu_misc.COMMA)
            pairs.append((row[0], set(row[1].split(' ')) ))
    return pairs

